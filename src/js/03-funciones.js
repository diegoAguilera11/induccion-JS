// Funciones declarativas
// Pueden ser llamadas antes de ser declaradas
// Funciones en Flecha
// Son utilizadas para funciones simples que retornan un valor

console.log(suma(3,2))

function suma(a, b) {
    return a + b
}

const arrowSuma = (a, b) => {
    return a + b
}

console.log(arrowSuma(7,2))

